package Week1Day2HomeAssign;

import java.util.Scanner;	

public class FibonnaciSeries {
	    public static void main(String[] args) {
	       int n=8;
	       int a=0,  b=1;
	       System.out.println(a+ ""+ b +"");
	       for (int i=2; i<n;i++) {
	    	   int c=a+b;
	    	   System.out.println(c+"");
	    	   a=b;
	    	   b=c;
	       }
	    }
	       }
	       
	       
	


